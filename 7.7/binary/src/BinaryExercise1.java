public class BinaryExercise1 {
    public static void main(String[] args){
        //演示四种进制
        //n1 二进制 以0b或0B开头
        int n1 = 0b1010;
        //n2 十进制
        int n2 = 1010;
        //n3 八进制 以0开头
        int n3 = 01010;
        //n4 十六进制 以0x或0X开头
        int n4 = 0x1a;
        System.out.println("n1="+n1);//10
        System.out.println("n2="+n2);//1010
        System.out.println("n3="+n3);//520
        System.out.println("n4="+n4);//65793
    }
}


