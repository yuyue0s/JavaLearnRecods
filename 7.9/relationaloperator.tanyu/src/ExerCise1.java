public class ExerCise1 {
    public static void main(String[] args){
        //演示关系运算符的使用
        int a = 8;
        int b = 9;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a == b);
        boolean flag = a > b;
        System.out.println("flag=" + flag);
    }
}
